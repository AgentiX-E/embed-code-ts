#!/usr/bin/env node
/**
 * Generate combined benchmark report (Node.js native vs WASM comparison).
 */
const fs = require('fs');

const args = process.argv.slice(2);
function getArg(flag) {
  const i = args.indexOf(flag);
  return i >= 0 ? args[i + 1] : null;
}

const nodePath = getArg('--node') || 'docs/benchmark/benchmark-report.json';
const webPath = getArg('--web') || 'docs/web-benchmark/web-benchmark-report.json';
const outPath = getArg('--out') || 'docs/benchmark/index.html';

if (!fs.existsSync(nodePath) || !fs.existsSync(webPath)) {
  console.log('[generate-combined-report] Missing report files — skipping');
  process.exit(0);
}

const node = JSON.parse(fs.readFileSync(nodePath, 'utf-8'));
const web = JSON.parse(fs.readFileSync(webPath, 'utf-8'));

function escapeHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

const nodeRows = (node.latency || [])
  .map(
    (c) =>
      `<tr><td>${escapeHtml(c.config)}</td><td>${c.avgLatencyMs}</td><td>${c.minLatencyMs}</td><td>${c.maxLatencyMs || '-'}</td></tr>`,
  )
  .join('\n');

const webRows = (web.latency || [])
  .map(
    (c) =>
      `<tr><td>${escapeHtml(c.config)}</td><td>${c.avgLatencyMs}</td><td>${c.minLatencyMs}</td><td>${c.maxLatencyMs || '-'}</td></tr>`,
  )
  .join('\n');

// Speedup
const speedups = [];
const nodeLat = node.latency || [];
const webLat = web.latency || [];
for (let i = 0; i < Math.min(nodeLat.length, webLat.length); i++) {
  const nLat = nodeLat[i].avgLatencyMs;
  const wLat = webLat[i].avgLatencyMs;
  speedups.push({
    config: nodeLat[i].config.replace('-wasm', ''),
    nodeMs: nLat,
    webMs: wLat,
    speedup: nLat > 0 ? (wLat / nLat).toFixed(2) + 'x' : 'N/A',
  });
}
const speedupRows = speedups
  .map(
    (s) =>
      `<tr><td>${escapeHtml(s.config)}</td><td>${s.nodeMs}ms</td><td>${s.webMs}ms</td><td>${s.speedup}</td></tr>`,
  )
  .join('\n');

const html = `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8"><title>embed-code-ts Benchmark Report</title>
<style>
*{margin:0;padding:0;box-sizing:border-box}
body{font-family:system-ui,sans-serif;max-width:960px;margin:2rem auto;padding:0 1rem;line-height:1.6;color:#1a1a2e}
h1{border-bottom:3px solid #2563eb;padding-bottom:.5rem}
h2{margin-top:2rem;border-bottom:2px solid #e5e7eb;padding-bottom:.25rem}
table{width:100%;border-collapse:collapse;margin:1rem 0}
th,td{padding:.5rem .75rem;text-align:left;border-bottom:1px solid #e5e7eb;font-size:.9rem}
th{background:#f5f5f5}
.green{color:#16a34a}.blue{color:#2563eb}.meta{color:#6b7280;font-size:.9rem}
</style>
</head>
<body>
<h1>📊 embed-code-ts Benchmark Report</h1>
<p class="meta">Model: ${escapeHtml(node.model || 'unknown')} | Dim: ${node.dim || 768} | ${escapeHtml(node.timestamp || '')}</p>

<h2>🔵 Node.js Native (onnxruntime-node, AVX2)</h2>
<table><tr><th>Config</th><th>Avg (ms)</th><th>Min (ms)</th><th>Max (ms)</th></tr>${nodeRows}</table>
${node.accuracy ? `<p class="green">Accuracy: query-doc=${node.accuracy.queryDocSimilarity}, query-unrelated=${node.accuracy.queryUnrelatedSimilarity} PASS</p>` : ''}

<h2>🟢 Web WASM (onnxruntime-web)</h2>
<table><tr><th>Config</th><th>Avg (ms)</th><th>Min (ms)</th><th>Max (ms)</th></tr>${webRows}</table>
${web.accuracy ? `<p class="green">Accuracy: query-doc=${web.accuracy.queryDocSimilarity}, query-unrelated=${web.accuracy.queryUnrelatedSimilarity} PASS</p>` : ''}

<h2>⚡ Native vs WASM Speedup</h2>
<table><tr><th>Config</th><th>Node.js Native</th><th>WASM</th><th>Speedup</th></tr>${speedupRows}</table>

<hr style="margin-top:2rem">
<p class="meta">Generated by embed-code-ts CI — <a href="https://github.com/AgentiX-E/embed-code-ts">GitHub</a></p>
</body>
</html>`;

fs.writeFileSync(outPath, html);
console.log('[generate-combined-report] Combined benchmark HTML generated at', outPath);
